# SCENARIO: SERIAL
# ═══════════════════════════════════════════════════════════════
def run_serial(model: str, prompts: list, scenario: str,
               gap: float, run_id: int,
               baseline: dict, device: str,
               completed: set = None) -> list:
    label = CFG["model_labels"].get(model, model)
    completed = completed or set()
    pending = [(i, p) for i, p in enumerate(prompts)
               if (model, scenario, run_id, i + 1) not in completed]
    if not pending:
        log.info("  [%s] %s — run %d/%d already complete, skipping",
                 scenario.upper(), label, run_id + 1, CFG["num_runs"])
        return []
    if len(pending) < len(prompts):
        log.info("  [%s] %s — run %d/%d resuming: %d/%d prompts remaining",
                 scenario.upper(), label, run_id + 1, CFG["num_runs"],
                 len(pending), len(prompts))
    else:
        log.info("  [%s] %s — run %d/%d (%d prompts)",
                 scenario.upper(), label, run_id + 1, CFG["num_runs"], len(prompts))
    cold_start_ms = warmup(model)
    results = []
    latencies = []
    energy_tracker = SessionEnergyTracker(name=f"{model}_{scenario}_run{run_id}")
    energy_tracker.start()
    for idx, (i, prompt) in enumerate(pending):
        row = run_single(model, prompt, device)
        row["scenario"] = scenario
        row["run_id"] = run_id
        row["request_index"] = i + 1
        row["cold_start_ms"] = cold_start_ms if i == 0 else 0.0
        results.append(row)
        latencies.append(row["latency_ms"])
        if gap > 0:
            time.sleep(gap)
    total_tokens = sum(r["output_tokens"] for r in results)
    energy_result = energy_tracker.stop(baseline, total_tokens)
    for row in results:
        row.update(energy_result)
    degs = compute_degradation(latencies)
    for row, deg in zip(results, degs):
        row["degradation_pct"] = deg
    return results


# ═══════════════════════════════════════════════════════════════
# SCENARIO: CONCURRENT
# ═══════════════════════════════════════════════════════════════
def run_concurrent(model: str, prompts: list, run_id: int,
                   baseline: dict, device: str,
                   completed: set = None) -> list:
    label = CFG["model_labels"].get(model, model)
    workers = CFG["concurrent_workers"]
    completed = completed or set()
    pending = [(i, p) for i, p in enumerate(prompts)
               if (model, "concurrent", run_id, i + 1) not in completed]
    if not pending:
        log.info("  [CONCURRENT] %s — run %d/%d already complete, skipping",
                 label, run_id + 1, CFG["num_runs"])
        return []
    if len(pending) < len(prompts):
        log.info("  [CONCURRENT] %s — run %d/%d resuming: %d/%d prompts remaining",
                 label, run_id + 1, CFG["num_runs"], len(pending), len(prompts))
    else:
        log.info("  [CONCURRENT] %s — run %d/%d (%d prompts, %d parallel)",
                 label, run_id + 1, CFG["num_runs"], len(prompts), workers)
    cold_start_ms = warmup(model)
    results = []
    lock = threading.Lock()
    energy_tracker = SessionEnergyTracker(name=f"{model}_concurrent_run{run_id}")
    energy_tracker.start()

    def worker(prompt, original_index):
        row = run_single(model, prompt, device)
        row["scenario"] = "concurrent"
        row["run_id"] = run_id
        row["request_index"] = original_index
        row["cold_start_ms"] = cold_start_ms if original_index == 1 else 0.0
        with lock:
            results.append(row)

    for start in range(0, len(pending), workers):
        batch = pending[start: start + workers]
        threads = [
            threading.Thread(target=worker, args=(p, i + 1))
            for i, p in batch
        ]
        for t in threads:
            t.start()
        for t in threads:
            t.join()
        time.sleep(0.2)
    total_tokens = sum(r["output_tokens"] for r in results)
    energy_result = energy_tracker.stop(baseline, total_tokens)
    for row in results:
        row.update(energy_result)
    results.sort(key=lambda r: r["request_index"])
    latencies = [r["latency_ms"] for r in results]
    degs = compute_degradation(latencies)
    for row, deg in zip(results, degs):
        row["degradation_pct"] = deg
    return results


# ═══════════════════════════════════════════════════════════════
# OUTLIER DETECTION
# ═══════════════════════════════════════════════════════════════
def flag_outliers(df: pd.DataFrame) -> pd.DataFrame:
    z_thr = CFG["outlier_z_threshold"]
    df = df.copy()
    df["is_outlier"] = False
    for _, grp in df.groupby(["model", "scenario"]):
        if len(grp) < 5:
            continue
        vals = grp["latency_ms"]
        z = np.abs((vals - vals.mean()) / (vals.std() + 1e-9))
        df.loc[z[z > z_thr].index, "is_outlier"] = True
    return df


# ═══════════════════════════════════════════════════════════════
# SUMMARY STATISTICS
# ═══════════════════════════════════════════════════════════════
def compute_summary(df: pd.DataFrame) -> pd.DataFrame:
    clean = df[~df["is_outlier"]]
    rows = []
    for model in CFG["models"]:
        label = CFG["model_labels"].get(model, model)
        for scenario in ["serial", "high_load", "concurrent"]:
            sub = clean[(clean["model"] == model) & (clean["scenario"] == scenario)]
            if sub.empty:
                continue
            lats = sub["latency_ms"]
            session_energy = (
                sub.groupby("run_id")["session_net_energy_j"].first()
            )
            session_ept = (
                sub.groupby("run_id")["session_energy_per_token_j"].first()
            )
            rows.append({
                "model": label,
                "scenario": scenario,
                "n_requests": len(sub),
                "latency_mean_ms": round(lats.mean(), 2),
                "latency_std_ms": round(lats.std(), 2),
                "latency_p50_ms": round(np.percentile(lats, 50), 2),
                "latency_p90_ms": round(np.percentile(lats, 90), 2),
                "latency_p99_ms": round(np.percentile(lats, 99), 2),
                "ttft_mean_ms": round(sub["ttft_ms"].mean(), 2),
                "decode_speed_mean": round(sub["decode_speed"].mean(), 2),
                "ram_peak_mean_gb": round(sub["ram_peak_gb"].mean(), 3),
                "cpu_peak_mean_pct": round(sub["cpu_peak_pct"].mean(), 2),
                "gpu_mem_peak_gb": round(sub["gpu_mem_peak_gb"].mean(), 3),
                "session_net_energy_mean_j": round(session_energy.mean(), 6),
                "session_net_energy_std_j": round(session_energy.std(), 6)
                if len(session_energy) > 1 else 0.0,
                "energy_per_tok_mean_j": round(session_ept.mean(), 7),
                "degradation_mean_pct": round(sub["degradation_pct"].mean(), 2),
                "cold_start_ms": round(sub[sub["cold_start_ms"] > 0]["cold_start_ms"].mean(), 2)
                if (sub["cold_start_ms"] > 0).any() else 0.0,
            })
    return pd.DataFrame(rows)


# ═══════════════════════════════════════════════════════════════
# CSV SAVE
# ═══════════════════════════════════════════════════════════════
def save_results(results: list, path: str):
    if not results:
        return
    fieldnames = list(results[0].keys())
    exists = os.path.exists(path)
    with open(path, "a", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        if not exists:
            writer.writeheader()
        writer.writerows(results)


def load_completed(csv_path: str) -> set:
    if not os.path.exists(csv_path):
        return set()
    try:
        df = pd.read_csv(csv_path,
                         usecols=["model", "scenario", "run_id", "request_index"])
        done = set()
        for _, row in df.drop_duplicates().iterrows():
            done.add((row["model"], row["scenario"],
                      int(row["run_id"]), int(row["request_index"])))
        return done
    except Exception:
        return set()


def session_complete(completed: set, model: str, scenario: str,
                     run_id: int, n_prompts: int) -> bool:
    return all((model, scenario, run_id, i) in completed
               for i in range(1, n_prompts + 1))


