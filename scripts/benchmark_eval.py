# IDLE BASELINE MEASUREMENT
# ═══════════════════════════════════════════════════════════════
def measure_idle_baseline(duration: int = 60) -> dict:
    cpu_samples = []
    for _ in range(duration * 2):
        cpu_samples.append(psutil.cpu_percent(interval=None))
        time.sleep(0.5)
    baseline = {
        "cpu_idle_pct": round(float(np.mean(cpu_samples)), 2),
        "duration_s": duration,
        "timestamp": datetime.now().isoformat(),
    }
    if HAS_GPU:
        try:
            baseline["gpu_idle_mem_mb"] = round(
                pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / 1024 ** 2, 1)
            log.info("  Idle baseline — CPU: %.1f%%  GPU idle VRAM: %.0fMB",
                     baseline["cpu_idle_pct"], baseline["gpu_idle_mem_mb"])
        except Exception:
            baseline["gpu_idle_mem_mb"] = None
            log.info("  Idle baseline — CPU: %.1f%%", baseline["cpu_idle_pct"])
    else:
        log.info("  Idle baseline — CPU: %.1f%%", baseline["cpu_idle_pct"])
    return baseline


