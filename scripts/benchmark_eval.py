# SESSION-LEVEL ENERGY TRACKING (CodeCarbon)
# ═══════════════════════════════════════════════════════════════
class SessionEnergyTracker:
    def __init__(self, name: str):
        self.name = name
        self._tracker = None
        self._wall_start = None

    def start(self):
        self._wall_start = time.time()
        if not HAS_CODECARBON:
            self._tracker = None
            return
        try:
            self._tracker = EmissionsTracker(
                project_name=self.name,
                measure_power_secs=1,
                save_to_file=False,
                log_level="error",
            )
            self._tracker.start()
        except Exception as e:
            log.warning("  [Energy] CodeCarbon failed to start: %s", e)
            self._tracker = None

    def stop(self, baseline: dict, total_tokens: int) -> dict:
        wall_end = time.time()
        wall_duration = max(wall_end - self._wall_start, 1e-6)
        total_energy_j = 0.0
        if self._tracker is not None:
            try:
                self._tracker.stop()
                ed = self._tracker.final_emissions_data
                if ed is not None and ed.energy_consumed is not None:
                    total_energy_j = ed.energy_consumed * 3_600_000
            except Exception as e:
                log.warning("  [Energy] CodeCarbon failed to stop: %s", e)
        idle_power_w = (baseline.get("cpu_idle_pct", 0) / 100.0) * CFG["cpu_tdp_watts"]
        idle_energy_j = idle_power_w * wall_duration
        net_energy_j = max(total_energy_j - idle_energy_j, 0.0)
        tok = max(total_tokens, 1)
        return {
            "session_total_energy_j": round(total_energy_j, 5),
            "session_net_energy_j": round(net_energy_j, 5),
            "session_energy_per_token_j": round(net_energy_j / tok, 7),
            "session_wall_duration_s": round(wall_duration, 3),
        }


