"""
benchmark_eval.py — LDWS Runtime Performance Benchmarking Script
=================================================================
Evaluating Small Language Models for Resource-Constrained Local Deployment
MSc Dissertation — Newcastle University
Supervisor: Dr Tomasz Szydlo

Purpose:
    Measures runtime performance of 5 SLMs across 3 workload scenarios using
    the LDWS 50-prompt set (generated by prepare_prompts.py). Capability
    evaluation (accuracy) is handled separately by lm-evaluation-harness.

    This script measures:
        - Inference Performance : TTFT, E2E latency, throughput (tok/s)
        - Memory Utilisation    : Peak RAM (RSS), CPU utilisation
        - Energy Consumption    : Via CodeCarbon, measured at session level
                                   (net energy per token)
        - Workload Stability    : Degradation rate, P50/P90/P99, cold start

Workload Scenarios:
    Serial     — 0.1s gap between requests (normal single-user usage)
    High Load  — no gap between requests (sustained high-frequency pressure)
    Concurrent — 4 parallel threads (multi-user shared device simulation)

Experimental Controls:
    - temperature=0, max_tokens=256 for all requests (deterministic output)
    - seed=42 for reproducibility
    - 3 warmup requests before each session (cold start recorded, rest discarded)
    - 3 repeated runs per configuration (mean ± std reported)
    - Idle baseline measured for 60s before experiments
    - Outlier detection: Z-score > 3.0 flagged and excluded from analysis

Energy Measurement (session-level):
    CodeCarbon's start/stop cycle has a fixed initialisation overhead of a
    few hundred milliseconds, which would dominate and distort per-request
    energy measurements (especially for fast 0.5B models). Energy is
    therefore tracked once per (model, scenario, run) session rather than
    per request:
        1. tracker.start() before the session loop
        2. all prompts in the session are processed
        3. tracker.stop() after the session loop
        4. total_energy_j = tracker.final_emissions_data.energy_consumed * 3.6e6
        5. idle baseline energy (covering the full wall-clock session
           duration, including inter-request gaps) is subtracted
        6. net_energy_j / total_tokens = session energy per token
    This session-level value is written to every row belonging to that
    session (session_net_energy_j, session_energy_per_token_j). Reported
    statistics (mean ± std) are computed across the `num_runs` repeated
    sessions per (model, scenario).

    Limitation: On Windows, RAPL is unavailable, so CodeCarbon falls back to
    TDP-based estimation for CPU power. This is noted as a limitation in the
    dissertation methodology chapter.

Usage:
    python benchmark_eval.py
    python benchmark_eval.py --runs 3
    python benchmark_eval.py --scenario serial
    python benchmark_eval.py --charts-only

Output:
    results/runtime_results.csv     — raw per-request data
    results/summary.csv             — aggregated stats per model/scenario
    results/device_map.json         — confirmed CPU/GPU inference device per model
    results/metadata.json           — experiment config + hardware environment
"""

