# OLLAMA API CALL
# ═══════════════════════════════════════════════════════════════
def query_ollama(model: str, prompt: str, retries: int = 3) -> dict:
    payload = {
        "model": model,
        "prompt": prompt,
        "stream": False,
        "options": {
            "temperature": CFG["temperature"],
            "num_predict": CFG["max_tokens"],
            "seed": CFG["seed"],
        },
    }
    empty = {
        "text": "", "latency_ms": 0.0, "ttft_ms": 0.0, "decode_ms": 0.0,
        "output_tokens": 0, "input_tokens": 0,
        "prefill_speed": 0.0, "decode_speed": 0.0,
        "model_load_ms": 0.0,
    }
    for attempt in range(retries):
        try:
            t0 = time.time()
            r = requests.post(CFG["ollama_url"], json=payload, timeout=180)
            latency_ms = round((time.time() - t0) * 1000, 2)
            d = r.json()
            text = d.get("response", "").strip()
            if not text:
                time.sleep(1)
                continue
            out_tok = d.get("eval_count", 0)
            in_tok = d.get("prompt_eval_count", 0)
            ttft_ms = round(d.get("prompt_eval_duration", 0) / 1e6, 2)
            dec_ms = round(d.get("eval_duration", 0) / 1e6, 2)
            load_ms = round(d.get("load_duration", 0) / 1e6, 2)
            pf_speed = round(in_tok / (ttft_ms / 1000 + 1e-9), 1) if in_tok else 0.0
            dc_speed = round(out_tok / (dec_ms / 1000 + 1e-9), 1) if out_tok else 0.0
            return {
                "text": text,
                "latency_ms": latency_ms,
                "ttft_ms": ttft_ms,
                "decode_ms": dec_ms,
                "output_tokens": out_tok,
                "input_tokens": in_tok,
                "prefill_speed": pf_speed,
                "decode_speed": dc_speed,
                "model_load_ms": load_ms,
            }
        except Exception as e:
            log.warning("  Ollama error (attempt %d/%d): %s", attempt + 1, retries, e)
            time.sleep(2)
    return empty


# ═══════════════════════════════════════════════════════════════
# WARMUP
# ═══════════════════════════════════════════════════════════════
def warmup(model: str) -> float:
    label = CFG["model_labels"].get(model, model)
    rounds = CFG["warmup_rounds"]
    log.info("  [Warmup] %s — %d rounds", label, rounds)
    t0 = time.time()
    query_ollama(model, "Hello.", retries=3)
    cold_start_ms = round((time.time() - t0) * 1000, 2)
    log.info("  [Cold start] %s: %.0f ms", label, cold_start_ms)
    for i in range(1, rounds):
        query_ollama(model, f"Warmup {i + 1}.", retries=2)
        time.sleep(0.3)
    time.sleep(0.5)
    return cold_start_ms


# ═══════════════════════════════════════════════════════════════
# DEGRADATION CALCULATION
# ═══════════════════════════════════════════════════════════════
def compute_degradation(latencies: list, baseline_n: int = 5) -> list:
    if len(latencies) < 2:
        return [0.0] * len(latencies)
    baseline = float(np.median(latencies[:baseline_n]))
    if baseline <= 0:
        return [0.0] * len(latencies)
    return [round((l - baseline) / baseline * 100, 2) for l in latencies]


