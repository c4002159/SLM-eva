def collect_hardware_info() -> dict:
    """
    Automatically collects hardware and software environment details at
    runtime and writes them into metadata.json alongside the experiment
    results.
    """
    info = {
        "os": platform.platform(),
        "python_version": platform.python_version(),
        "cpu": platform.processor(),
        "cpu_cores_physical": psutil.cpu_count(logical=False),
        "cpu_cores_logical": psutil.cpu_count(logical=True),
        "ram_total_gb": round(psutil.virtual_memory().total / 1024 ** 3, 2),
    }
    if HAS_GPU:
        try:
            info["gpu_name"] = pynvml.nvmlDeviceGetName(_gpu_handle)
            info["gpu_vram_total_gb"] = round(
                pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).total / 1024 ** 3, 2
            )
            info["gpu_driver_version"] = pynvml.nvmlSystemGetDriverVersion()
        except Exception as e:
            info["gpu_name"] = f"GPU detected but info unavailable: {e}"
    else:
        info["gpu_name"] = "N/A (GPU not detected or pynvml unavailable)"
    try:
        import ollama
        info["ollama_client_version"] = ollama.__version__
    except Exception:
        info["ollama_client_version"] = "N/A (ollama Python package not installed)"
    log.info("  [Hardware] OS: %s", info["os"])
    log.info("  [Hardware] CPU: %s (%d physical / %d logical cores)",
             info["cpu"], info["cpu_cores_physical"], info["cpu_cores_logical"])
    log.info("  [Hardware] RAM: %.2f GB", info["ram_total_gb"])
    log.info("  [Hardware] GPU: %s", info["gpu_name"])
    return info


