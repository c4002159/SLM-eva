def detect_inference_device(model: str) -> str:
    if not HAS_GPU:
        log.info("  [Device] %s → CPU (pynvml unavailable)", model)
        return "CPU"
    try:
        idle_mem = pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / 1024 ** 2
        idle_util = pynvml.nvmlDeviceGetUtilizationRates(_gpu_handle).gpu
        requests.post(
            CFG["ollama_url"],
            json={"model": model, "prompt": "Hi",
                  "options": {"num_predict": 5, "temperature": 0}},
            timeout=60,
        )
        max_wait_s = 10
        poll_interval_s = 0.5
        elapsed = 0.0
        loaded_mem, loaded_util = idle_mem, idle_util
        while elapsed < max_wait_s:
            time.sleep(poll_interval_s)
            elapsed += poll_interval_s
            loaded_mem = pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / 1024 ** 2
            loaded_util = pynvml.nvmlDeviceGetUtilizationRates(_gpu_handle).gpu
            if (loaded_mem - idle_mem) > 200:
                break
        mem_delta = loaded_mem - idle_mem
        util_delta = loaded_util - idle_util
        device = "GPU" if (mem_delta > 200 or util_delta > 10) else "CPU"
        log.info("  [Device] %-20s → %s  (ΔVRAM=%.0f MB, ΔUtil=%d%%, waited=%.1fs)",
                 model, device, mem_delta, util_delta, elapsed)
        return device
    except Exception as e:
        log.warning("  [Device] Detection failed for %s: %s", model, e)
        return "CPU"


def unload_model_and_wait(model: str, max_wait_s: int = 15):
    """
    Unloads a model from Ollama and polls GPU memory until it returns
    to a stable near-idle state, before the next model is loaded.
    """
    try:
        requests.post(
            CFG["ollama_url"],
            json={"model": model, "prompt": "", "keep_alive": 0},
            timeout=10,
        )
    except Exception:
        pass
    if not HAS_GPU:
        time.sleep(3)
        return
    try:
        start_mem = pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / 1024 ** 2
        elapsed = 0.0
        poll_interval = 0.5
        prev_mem = start_mem
        stable_count = 0
        while elapsed < max_wait_s:
            time.sleep(poll_interval)
            elapsed += poll_interval
            cur_mem = pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / 1024 ** 2
            if abs(cur_mem - prev_mem) < 50:
                stable_count += 1
                if stable_count >= 3:
                    break
            else:
                stable_count = 0
            prev_mem = cur_mem
        log.info("  [Unload] %-20s VRAM %.0f→%.0fMB (waited %.1fs)",
                 model, start_mem, prev_mem, elapsed)
    except Exception as e:
        log.warning("  [Unload] GPU memory polling failed: %s", e)
        time.sleep(3)


def detect_all_devices() -> dict:
    log.info("\n=== Phase 0: Inference Device Detection ===")
    device_map = {}
    for model in CFG["models"]:
        device_map[model] = detect_inference_device(model)
        # 【修改点 1】：在检测完当前模型后，立刻卸载它，避免污染下一个模型的检测基线
        unload_model_and_wait(model)

    out = os.path.join(CFG["output_dir"], "device_map.json")
    with open(out, "w") as f:
        json.dump(device_map, f, indent=2)
    log.info("  Device map saved to device_map.json")
    return device_map


