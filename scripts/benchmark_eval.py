# SYSTEM METRICS COLLECTOR (background thread)
# ═══════════════════════════════════════════════════════════════
class MetricsCollector:
    def __init__(self, interval_ms: int = 50):
        self.interval = interval_ms / 1000.0
        self._running = False
        self._thread = None
        self._cpu = []
        self._ram = []
        self._gpu_util = []
        self._gpu_mem = []
        self._gpu_pow = []
        self._ts = []
        self._ollama_procs = []

    def _find_ollama(self):
        ollama_processes = []
        ollama_keywords = ["ollama", "ollama_llama_server"]
        for p in psutil.process_iter(["name", "pid"]):
            try:
                process_name = (p.info["name"] or "").lower()
                if any(keyword in process_name for keyword in ollama_keywords):
                    ollama_processes.append(p)
            except Exception:
                pass
        return ollama_processes

    def start(self):
        self._cpu = [];
        self._ram = [];
        self._ts = []
        self._gpu_util = [];
        self._gpu_mem = [];
        self._gpu_pow = []
        self._ollama_procs = self._find_ollama()
        self._running = True
        self._thread = threading.Thread(target=self._loop, daemon=True)
        self._thread.start()

    def _loop(self):
        while self._running:
            t = time.time()
            cpu = psutil.cpu_percent(interval=None)
            self._ollama_procs = self._find_ollama()
            ram = 0.0
            for p in self._ollama_procs:
                try:
                    ram += p.memory_info().rss / (1024 ** 3)
                except (psutil.NoSuchProcess, psutil.AccessDenied):
                    pass
            if ram == 0.0:
                log.warning("  [MetricsCollector] Ollama process lost — using system RAM fallback")
                ram = psutil.virtual_memory().used / (1024 ** 3)
            self._cpu.append(cpu)
            self._ram.append(ram)
            self._ts.append(t)
            if HAS_GPU:
                try:
                    gu = pynvml.nvmlDeviceGetUtilizationRates(_gpu_handle).gpu
                    gm = pynvml.nvmlDeviceGetMemoryInfo(_gpu_handle).used / (1024 ** 3)
                    gp = pynvml.nvmlDeviceGetPowerUsage(_gpu_handle) / 1000.0
                except Exception:
                    gu, gm, gp = 0.0, 0.0, 0.0
                self._gpu_util.append(gu)
                self._gpu_mem.append(gm)
                self._gpu_pow.append(gp)
            time.sleep(self.interval)

    def stop(self) -> dict:
        self._running = False
        if self._thread:
            self._thread.join(timeout=2)
        if not self._cpu:
            return {
                "cpu_peak_pct": 0.0, "cpu_avg_pct": 0.0,
                "ram_peak_gb": 0.0,
                "gpu_util_peak": 0.0, "gpu_mem_peak_gb": 0.0,
                "gpu_power_avg_w": 0.0,
                "n_samples": 0,
            }
        return {
            "cpu_peak_pct": round(max(self._cpu), 2),
            "cpu_avg_pct": round(float(np.mean(self._cpu)), 2),
            "ram_peak_gb": round(max(self._ram), 3),
            "gpu_util_peak": round(max(self._gpu_util), 1) if self._gpu_util else 0.0,
            "gpu_mem_peak_gb": round(max(self._gpu_mem), 3) if self._gpu_mem else 0.0,
            "gpu_power_avg_w": round(float(np.mean(self._gpu_pow)), 2) if self._gpu_pow else 0.0,
            "n_samples": len(self._cpu),
        }


